package com.shuxin.model.vo;

public class PatientCheckVo {
	
	private String projectName;
	private String department;
	private String medicareType;
	private String diagnosis;
	private String patientName;
	private String docotorEmp;
	private String docotorName;
	private String visitType;
	
	
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getMedicareType() {
		return medicareType;
	}
	public void setMedicareType(String medicareType) {
		this.medicareType = medicareType;
	}
	public String getDiagnosis() {
		return diagnosis;
	}
	public void setDiagnosis(String diagnosis) {
		this.diagnosis = diagnosis;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public String getDocotorEmp() {
		return docotorEmp;
	}
	public void setDocotorEmp(String docotorEmp) {
		this.docotorEmp = docotorEmp;
	}
	public String getDocotorName() {
		return docotorName;
	}
	public void setDocotorName(String docotorName) {
		this.docotorName = docotorName;
	}
	public String getVisitType() {
		return visitType;
	}
	public void setVisitType(String visitType) {
		this.visitType = visitType;
	}

}
